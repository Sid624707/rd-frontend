/*
 * 2. Opposite number
 
 * DESCRIPTION:
    Very simple, given an integer or a floating-point number, find its opposite.

    Examples:
        1: -1
        14: -14
        -34: 34
 */

function oppositeNumber(number) {
    return -1 * number;
}

var num = -1.235463;
console.log(oppositeNumber(num));
